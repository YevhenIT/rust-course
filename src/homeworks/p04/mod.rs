fn main() {
    // Встановлюємо розміри ромба
    const WIDTH: usize = 15;  // Ширина ромба (непарне число)
    const HEIGHT: usize = 7;  // Висота ромба (парне число)

    // Малюємо верхню частину ромба
    for i in 0..HEIGHT {
        // Малюємо пробіли зліва
        for _ in 0..(HEIGHT - i - 1) {
            print!(" ");
        }

        // Малюємо зірочки
        for _ in 0..(2 * i + 1) {
            print!("*");
        }

        // Переходимо на новий рядок
        println!();
    }

    // Малюємо нижню частину ромба
    for i in (0..HEIGHT - 1).rev() {
        // Малюємо пробіли зліва
        for _ in 0..(HEIGHT - i - 1) {
            print!(" ");
        }

        // Малюємо зірочки
        for _ in 0..(2 * i + 1) {
            print!("*");
        }

        // Переходимо на новий рядок
        println!();
    }
}
